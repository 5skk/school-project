function getLanguage() {
  const savedLanguage = localStorage.getItem('language');
  return translations[savedLanguage] ? savedLanguage : 'en';
}

function translate(key) {
  const lang = getLanguage();
  return translations[lang][key] || translations.en[key] || key;
}

function applyLanguage(lang) {
  const selectedLanguage = translations[lang] ? lang : 'en';
  localStorage.setItem('language', selectedLanguage);
  document.documentElement.lang = selectedLanguage;

  document.querySelectorAll('[data-text]').forEach((element) => {
    element.textContent = translations[selectedLanguage][element.dataset.text] || '';
  });

  document.querySelectorAll('[data-placeholder]').forEach((element) => {
    element.placeholder = translations[selectedLanguage][element.dataset.placeholder] || '';
  });

  document.querySelectorAll('[data-label]').forEach((element) => {
    element.setAttribute('aria-label', translations[selectedLanguage][element.dataset.label] || '');
  });

  document.querySelectorAll('[data-lang-option]').forEach((button) => {
    button.classList.toggle('active', button.dataset.langOption === selectedLanguage);
  });

  const pageName = document.body.dataset.page;
  if (pageName && translations[selectedLanguage].pageTitles[pageName]) {
    document.title = translations[selectedLanguage].pageTitles[pageName];
  }
}

function setupLanguageSwitcher() {
  const toggle = document.getElementById('languageToggle');
  const panel = document.getElementById('languagePanel');

  if (!toggle || !panel) {
    return;
  }

  toggle.addEventListener('click', () => {
    const isOpen = panel.classList.toggle('open');
    panel.hidden = false;
    toggle.setAttribute('aria-expanded', String(isOpen));
  });

  document.querySelectorAll('[data-lang-option]').forEach((button) => {
    button.addEventListener('click', () => {
      applyLanguage(button.dataset.langOption);
      panel.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    });
  });

  document.addEventListener('click', (event) => {
    if (!event.target.closest('.language-switcher')) {
      panel.classList.remove('open');
      toggle.setAttribute('aria-expanded', 'false');
    }
  });
}

document.addEventListener('DOMContentLoaded', () => {
  setupLanguageSwitcher();
  applyLanguage(getLanguage());
});
